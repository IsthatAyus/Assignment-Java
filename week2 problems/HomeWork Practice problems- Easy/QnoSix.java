// 6. Comparison using if-else.Write a Java program that takes two numbers and prints the greater number.
public class QnoSix {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;

        if (num1 > num2) {
            System.out.println("The greater number is: " + num1);
        } else {
            System.out.println("The greater number is: " + num2);
        }
    }
}
